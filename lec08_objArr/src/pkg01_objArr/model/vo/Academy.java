package pkg01_objArr.model.vo;

public class Academy {
	private String name;
	private String phone;
	
	public Academy() {}
	
	public Academy(String name, String phone) {
		this.name = name;
		this.phone = phone;
	}
	
	public String getName() {
		return name;
	}
	
	public String getPhone() {
		return phone;
	}
			
}
